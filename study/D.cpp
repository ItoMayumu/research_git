#include <bits/stdc++.h>

using namespace std;

int main(){
    cout << "Hello, World!" << endl;
    cout << 1+1 << endl;
    cout << 3-4 << endl;
    cout << 5*6 << endl;
    cout << 7/2 << endl;
    // 切り捨てないで割り算をしたいときは、少なくとも片方を小数にする
    cout << 7/2.0 << endl;
    cout << 4%3 << endl;
    cout << 100 * (100 + 1) / 2 << endl;
}


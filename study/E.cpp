#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
    int seconds = 365 * 24 * 60 * 60;

    int one_year = seconds;
    int two_years = 2 * seconds;
    int five_years = 5 * seconds;
    int ten_years = 10 * seconds;

  cout << one_year << endl;
  cout << two_years << endl;
  cout << five_years << endl;
  cout << ten_years << endl;
}
